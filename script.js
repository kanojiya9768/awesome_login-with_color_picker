let green = () =>{
  document.documentElement.style.setProperty('--bg_color','#00ff48');
}

let pink = () =>{
  document.documentElement.style.setProperty('--bg_color','#ff0062');
}

let violet = () =>{
  document.documentElement.style.setProperty('--bg_color','#ff00cc');
}

let yellow = () =>{
  document.documentElement.style.setProperty('--bg_color','#fff200');
  document.documentElement.style.setProperty('--text_color','#000');
}

let orange = () =>{
  document.documentElement.style.setProperty('--bg_color','#ff6a00');
}

let black = () =>{
  document.documentElement.style.setProperty('--bg_color','#000');
   document.documentElement.style.setProperty('--text_color','#fff');
}
